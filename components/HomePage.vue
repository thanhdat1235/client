export default {
  mixins: [validationMixin],
  name: "HomePage",
  components: {
    Flickity,
  },
  data() {
    return {
      flickityOptions: {
        initialIndex: 3,
        prevNextButtons: false,
        pageDots: false,
        wrapAround: true,
      },
      name: "",
      phonenumber: "",
      demand: "",
      errorContact: "",
    };
  },
  validations: {
    name: {
      required,
    },
    phonenumber: {
      required,
    },
    demand: {
      required,
    },
  },
  methods: {
    next() {
      this.$refs.flickity.next();
    },

    previous() {
      this.$refs.flickity.previous();
    },

    async contactSubmit() {
      this.$v.$touch();
      try {
        const contact = await contactService.create({
          name: this.name,
          phonenumber: parseInt(this.phonenumber),
          demand: this.demand,
        });
        this.errorContact = "Gửi thông tin thành công";
      } catch (error) {
        console.log(error);
        this.error = "Đã xảy ra lỗi vui lòng thử lại";
      }
    },
  },
};
